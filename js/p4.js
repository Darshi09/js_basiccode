// Write a JavaScript code snippet that uses the compound assignment operators to perform the following operations:

// Add 15 to num
// Subtract 5 from num
// Multiply num by 3
// Divide num by 2
// Initialize num to 10, and log the result after each operation.

let num = 10;

num += 15; 
console.log("After addition:", num);

num -= 5;  
console.log("After subtraction:", num); 

num *= 3;   
console.log("After multiplication:", num); 

num /= 2;   
console.log("After division:", num); 

// After addition: 25
// After subtraction: 20
// After multiplication: 60
// After division: 30
